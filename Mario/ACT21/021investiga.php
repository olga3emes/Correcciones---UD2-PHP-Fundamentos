<?php 

/* ucwords — Convierte a mayúsculas el primer caracter de cada palabra de una cadena 

Ejemplo: */

$cadena = "hola mundo";
echo "Ejemplo 1: " .ucwords($cadena);

echo "<br><br>";

/* strrev — Invierte una string

Ejemplo: */

$cadena2 = "hola mundo";
echo "Ejemplo 2: " .strrev($cadena2);

echo "<br><br>";

/* str_repeat — Repite un string

Ejemplo: */

$cadena3 = "hola mundo";
echo "Ejemplo 3: " .str_repeat($cadena, 4);

echo "<br><br>";

/* md5 — Calcula el 'hash' md5 de un string

Ejemplo: */

$cadena4 = "hola mundo";

echo "Ejemplo 4: " .md5($cadena4);

?>